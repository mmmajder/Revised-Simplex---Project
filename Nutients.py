class Nutrients:
    def __init__(self, calories, proteins_min, proteins_max, carbs_min, carbs_max, fats_min, fats_max):
        self.calories = calories
        self.proteins_min = proteins_min
        self.proteins_max = proteins_max
        self.carbs_min = carbs_min
        self.carbs_max = carbs_max
        self.fats_min = fats_min
        self.fats_max = fats_max